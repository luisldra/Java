package Taller_Interfaces_1.Ejercicio_1;

public class Mascota {
    
    Double peso;
    char tamaño;
    public Mascota(Double peso, char tamaño) {
        this.peso = peso;
        this.tamaño = tamaño;
    }
    public Double getPeso() {
        return peso;
    }
    public char getTamaño() {
        return tamaño;
    }

    


}
